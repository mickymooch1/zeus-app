@asynccontextmanager
async def lifespan(app: FastAPI):
    global history
    log.info("Startup: initialising HistoryStore")
    _sensitive = {"ANTHROPIC_API_KEY", "SECRET_KEY", "DATABASE_URL", "PASSWORD", "JWT_SECRET",
                  "STRIPE_SECRET_KEY", "STRIPE_WEBHOOK_SECRET"}
    for _k, _v in sorted(os.environ.items()):
        _display = f"***({len(_v)} chars)" if _k in _sensitive else _v
        log.info("  ENV %s=%s", _k, _display)
    try:
        history = HistoryStore()
        log.info("HistoryStore ready at %s", history.dir)
    except Exception:
        log.exception("FATAL: HistoryStore init failed")
        raise

    # Initialise user tables in the same DB file
    try:
        _db_path = db.get_db_path()
        db.init_user_tables(_db_path)
        log.info("User tables initialised at %s", _db_path)
        db.fail_stale_tasks(_db_path)
        log.info("Stale running tasks marked as failed")
        # Ensure owner account always has enterprise + admin access
        _owner_email = "dominic.rowle@yahoo.com"
        _owner = db.get_user_by_email(_db_path, _owner_email)
        if _owner:
            db.update_user(_db_path, _owner["id"],
                           subscription_plan="enterprise",
                           subscription_status="active",
                           is_admin=1)
            log.info("Owner account %s set to enterprise/admin", _owner_email)
        else:
            log.info("Owner account %s not yet registered — will be promoted on first login", _owner_email)
    except Exception:
        log.exception("FATAL: user table init failed")
        raise

    api_key_set = bool(os.environ.get("ANTHROPIC_API_KEY", "").strip())
    log.info("ANTHROPIC_API_KEY present: %s", api_key_set)
    if not api_key_set:
        raise RuntimeError("ANTHROPIC_API_KEY is not set — add it to Railway → Service → Variables")
    get_anthropic_client()  # validate key and warm up client at startup
    log.info("Anthropic client initialised")
    try:
        _scheduler_mod.init_scheduler(history)
        log.info("Scheduler initialised")
        if _RAILWAY:
            log.info("Running on Railway — skipping cloudflared tunnel (not installed)")
            yield
        else:
            port = int(os.environ.get("PORT", 8000))
            task = asyncio.create_task(start_tunnel_background(port))
            _background_tasks.add(task)
            task.add_done_callback(_background_tasks.discard)
            yield
            task.cancel()
            stop_tunnel()
    finally:
        _scheduler_mod.shutdown_scheduler()
        log.info("Scheduler shut down")


