_ALLOWED_ADMIN_FIELDS = {
    "subscription_plan": {"free", "pro", "agency", "enterprise"},
    "subscription_status": {"free", "active", "cancelled"},
}


@app.patch("/admin/users/{user_id}")
async def admin_patch_user(
    user_id: str,
    body: AdminUserPatchRequest,
    current_user: dict = Depends(auth.get_current_user),
):
    if not current_user.get("is_admin"):
        raise HTTPException(status_code=403, detail="Admin access required")
    allowed_values = _ALLOWED_ADMIN_FIELDS.get(body.field)
    if allowed_values is None:
        raise HTTPException(status_code=400, detail=f"Field '{body.field}' is not editable")
    if body.value not in allowed_values:
        raise HTTPException(
            status_code=400,
            detail=f"Value '{body.value}' not allowed for '{body.field}'. Allowed: {sorted(allowed_values)}",
        )
    db_path = db.get_db_path()
    target = db.get_user_by_id(db_path, user_id)
    if not target:
        raise HTTPException(status_code=404, detail="User not found")
    db.update_user(db_path, user_id, **{body.field: body.value})
    log.info("admin_patch_user: set %s.%s = %s (by admin %s)", user_id, body.field, body.value, current_user["id"])
    return {"ok": True}


@app.get("/admin/credits")
async def admin_credits(current_user: dict = Depends(auth.get_current_user)):
    if not current_user.get("is_admin"):
        raise HTTPException(status_code=403, detail="Admin access required")
    api_key = os.environ.get("ANTHROPIC_API_KEY", "").strip()
    if not api_key:
        return {"balance": None, "message": "Balance unavailable"}
    try:
        async with httpx.AsyncClient() as client:
            resp = await client.get(
                "https://api.anthropic.com/v1/organizations/credits/balance",
                headers={
                    "x-api-key": api_key,
                    "anthropic-version": "2023-06-01",
                },
                timeout=10,
            )
        if resp.status_code != 200:
            return {"balance": None, "message": "Balance unavailable"}
        data = resp.json()
        # Response shape: {"balance": {"available": [{"amount": 1234, "currency": "USD"}]}}
        available = data.get("balance", {}).get("available", [])
        if available:
            amount_cents = available[0].get("amount", 0)
            currency = available[0].get("currency", "USD")
            return {"balance": round(amount_cents / 100, 2), "currency": currency}
        return {"balance": None, "message": "Balance unavailable"}
    except Exception:
        log.exception("admin_credits: failed to fetch Anthropic balance")
        return {"balance": None, "message": "Balance unavailable"}


